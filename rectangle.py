def area(a, b):
    '''Принимает значения a и b(длины сторон ),находит площадь прямоугольника.
    К примеру,для сторон со значениями 5 и 4 print(area(5,4))найдет площадь прямоугольника
     (равна 20)
    '''
    return a * b

def perimeter(a, b):
    '''Принимает значения a и b(длины сторон),находит периметр прямоугольника.
    Для сторон со значениями 2 и 8 print(perimeter(2,8)) выведет периметр
     прямоугольника,равный 20
    '''
    return 2 * (a + b)



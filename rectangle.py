def area(a, b):
    ''' переменные a и b принимают значения сторон прямоугольника
        функция возвращает произведение a и b, т.е. площадь прямоугольника'''
    return a * b

def perimeter(a, b):
    ''' переменные a и b принимают значения сторон прямоугольника
        функция возвращает удвоенную сумму a и b, т.е. периметр прямоугольника'''
    return 2*(a + b)

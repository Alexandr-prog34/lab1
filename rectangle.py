def area(a, b):
    '''Returns the area of the rectangle.

            Parameters:
                a (int): first side of the rectangle
                b (int): second side og the rectangle
            Returns:
                (int): area of the rectangle
    '''

    return a * b 

def perimeter(a, b):
    '''Returns the perimeter of the rectangle.

            Parameters:
                a (int): first side of the rectangle
                b (int): second side og the rectangle
            Returns:
                (int): perimeter of the rectangle
    '''

    return 2 * (a + b)
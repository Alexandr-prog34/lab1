def area(a, b):
    '''
    Принимает два числа a и b - стороны прямоугольника,
    возвращает произведение двух чисел - площадь прямоугольника
    '''
    return a * b

def perimeter(a, b):
    '''
    Принимает два числа a и b - стороны прямоугольника,
    возращает сумму двух чисел домноженную на два
    '''
    return (a + b) * 2

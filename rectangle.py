def area(a, b):
     '''
     Принимает два числа, которые являются сторонами прямоугольника: a, b
     Возвращает площадь прямоугольника, перемножая число a на число b: a * b
     '''
     return a * b
def perimeter(a, b):
     '''
     Принимает два числа, которые являются сторонами прямоугольника: a, b
     Возвращает периметр прямоугольника, вычисляя его по формуле: P = 2 * (a + b)
     '''
     return 2 * (a + b)

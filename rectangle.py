def area(a, b):
    '''Provides an area of the rectangle using the length of its sides

    print(area(4, 5))
    Output: 20

    '''
    return a * b

def perimeter(a, b):
    '''Provides perimeter of the rectangle using the length of its sides

    print(perimeter(4, 5))
    Output: 18

    '''
    return a * 2 + b * 2
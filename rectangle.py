def area(a, b):
    """
        Возвращает площадь прямоугольника со стороной a и стороной b
        Параметры:
            а (int) : сторона 
            b (int) : сторона прямоугольника
    """
    return a * b 

def perimeter(a, b): 
    """
        Возвращает периметр прямоугольника со стороной a и стороной b
        Параметры:
            а (int) : сторона прямоугольника
            b (int) : сторона прямоугольника
    """
    return 2 * (a + b) 

def area(a):
    '''Returns the area of the square.

            Parameters:
                a (int): side of the square
            Returns:
                (int): area of the square
        
        Example:
            input -> 2 (a)
            output -> 4 (a * a)
    '''

    return a * a


def perimeter(a):
    '''Returns the perimeter of the square.

            Parameters:
                a (int): side of the square
            Returns:
                (int): perimeter of the square
                
        Example:
            input -> 2 (a)
            output -> 8 (4 * a)
    '''

    return 4 * a

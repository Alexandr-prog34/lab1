def area(a, h):
    '''Provides an area of the triangle using the length of its side and its height

    print(area(5, 4)
    Output: 10

    '''
    return a * h / 2

def perimeter(a, b, c):
    '''Provides perimeter of the triangle using the length of its sides

    print(perimeter(3, 4, 5))
    Output: 12

    '''
    return a + b + c
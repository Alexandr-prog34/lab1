def area(a, h):
    """
    Приниманет два параметра a и h - редро и высоту треугольника,
    возвращает полщадь данного треугольника.
    """
    return a * h / 2

def perimeter(a, b, c):
    """
    Принимает три параметра a, b и c - рёбра треугольника,
    возвращает периметр данного треугольника.
    """
    return a + b + c

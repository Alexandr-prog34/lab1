def area(a, h):
    '''Принимает числа a и h (сторона и высота, перпендикулярная стороне, соотвественно), возвращает площадь треугольника'''
    return a * h / 2
def perimeter(a, b, c):
    '''Принимает числа a, b и c (три стороны треугольника), возвращает периметр треугольника'''
    return a + b + c


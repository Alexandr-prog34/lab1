def area(a, b):
    '''
    Принимает числа а и b - стороны прямоугольника
    Возвращает площадь прямоугольника
    Пример: a=4, b=4, area(4,4)=16
    '''
    return a * b

def perimeter(a, b):
    '''
    Принимает числа а и b - стороны прямоугольника
    Возвращает периметр прямоугольника
    Пример: a=4, b=4, perimeter(4,4)=16
    '''
    return (a + b) * 2
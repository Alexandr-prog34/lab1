def area(a, h):
    '''
    Принимает числа a(сторона треугольника) и h(высота треугольника, проведённая к этой стороне)
    Возвращает площадь треугольника a * h / 2
    '''
    return a * h / 2
def perimeter(a, b, c):
    '''
    Принимает числа a, b, c(стороны треугольника)
    Возвращает периметр a + b + c
    '''
    return a + b + c

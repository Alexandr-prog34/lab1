import math


def area(r):
    '''
    возвращает плоащдь круга
	На вход подается число r:
	На выходе получаем площадь круга
    '''
	
    return math.pi * r * r


def perimeter(r):
    '''
	Возращает длину круга
		На вход подается число r
		На выходе получают длину круга.
   '''
    return 2 * math.pi * r


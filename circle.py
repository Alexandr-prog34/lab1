import math


def area(r):
    '''Returns the area of the circle.

            Parameters:
                r (int): radius of the circle
            Returns:
                (int): area of the circle
    '''

    return math.pi * r * r


def perimeter(r):
    '''Returns the perimeter of the circle.

            Parameters:
                r (int): radius of the circle
            Returns:
                (int): perimeter of the circle
    '''

    return 2 * math.pi * r

